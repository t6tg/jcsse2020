                                Email : <a href="mailto:y2.li@qut.edu.au">y2.li@qut.edu.au</a>
                            </div>
                        </div>
                        <br><b>Biography:</b><br>
                        <span>Professor Yuefeng Li is the leader of AI-Based Data Analysis Group in the School of
                            Electrical
                            Engineering and Computer Science, QUT. He has published over 190 refereed papers
                            (including 65 journal papers). He has demonstrable experience in leading large-scale
                            research
                            projects and has achieved many established research outcomes that have been published and
                            highly cited in many significant Journals and Conferences. He has been a program chair of
                            several International Conferences and workshops. He is the Editor-in-Chief of Web
                            Intelligence Journal.</span>
                        <br><br>
                        <b>Research Areas:</b>
                        <ul>
                            <li><b>Text mining and analysis</b></li>
                            <span>His contributions comprise pattern based models and relevance feature discovery to
                                improve
                                the effectiveness of information filtering. Another major contribution has been
                                introducing
                                theory and model into text classification research and document summarization from the
                                field
                                of three-way decision theory.</span>
                            <li><b>Topic Modelling and Feature Selection</b></li>
                            <span>His contributions include models for integrating LDA (Latent Dirichlet Allocation, a
                                famous
                                topic modelling method) and pattern mining, and using extended random sets to interpret
                                latent
                                LDA topics to enhance the performance of topic modelling and feature selection.</span>
                            <li><b>Ontology Learning and Web Intelligence</b></li>
                            <span>Ontology learning is an automatic or semi-automatic process for building of ontologies
                                (knowledge bases) to enhance text analysis and support conversation between machine and
                                human being. His contributions comprise ontology learning frameworks for information
                                gathering and a new methodology for personalized ontology construction and its
                                application
                                for the interpretation of text mining results.</span>
                            <li><b>Foundations of Data mining</b></li>
                            <span>His contributions include algorithms for mining non-redundant rules and a theory for
                                extending
                                association rule mining and clustering into granule mining.</span>
                        </ul>
